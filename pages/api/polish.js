export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).end();

  const { text, anthropicKey } = req.body;
  if (!text) return res.status(400).json({ error: 'Missing text' });

  const key = anthropicKey || process.env.ANTHROPIC_API_KEY;
  if (!key) return res.status(400).json({ error: 'Missing Anthropic key' });

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': key,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 1000,
        messages: [{
          role: 'user',
          content: `Ти си експерт редактор на диктован текст на български език. Получаваш суров текст от гласово разпознаване — без пунктуация, без главни букви — и трябва да го превърнеш в правилно написан текст.

ЗАДЪЛЖИТЕЛНИ ПРАВИЛА ЗА ПУНКТУАЦИЯ:

1. ТОЧКИ — постави точка в края на всяко завършено изречение. Ако изречението завършва с въпрос → въпросителна. Ако е възклицание → удивителна.

2. ГЛАВНИ БУКВИ — ВСЯКО ново изречение започва с главна буква. Собствени имена на хора, градове, държави, фирми — главна буква.

3. ЗАПЕТАИ — постави запетая:
   - пред "но", "а", "или", "обаче", "защото", "тъй като", "въпреки че", "ако", "когато", "докато", "след като", "преди да"
   - при изброяване: "ябълки, круши и сливи"
   - след уводни изрази: "всъщност,", "например,", "разбира се,", "между другото,"
   - при вметнати изрази и пояснения

4. ДВОЕТОЧИЕ — след "следното:", "например:", "а именно:" и преди изброяване

5. ТИРЕ — при рязка смяна на мисълта или при пояснение в средата на изречение

ДОПЪЛНИТЕЛНИ ПРАВИЛА:
- Премахни "ъ", "ем", "ъъъ", "значи" в началото на изречения (filler думи)
- Премахни повтарящи се думи ("трябва трябва" → "трябва")
- НЕ добавяй нови думи, НЕ променяй смисъла
- НЕ добавяй форматиране (без болд, без списъци) освен ако текстът очевидно е списък
- Върни САМО готовия текст — без обяснения, без коментари

ПРИМЕР:
Вход: "днес отидох до магазина купих хляб мляко и масло после се върнах вкъщи беше много студено навън"
Изход: "Днес отидох до магазина, купих хляб, мляко и масло. После се върнах вкъщи. Беше много студено навън."

Суров текст: ${text}`
        }]
      })
    });

    const data = await response.json();
    if (!response.ok) return res.status(response.status).json(data);
    return res.status(200).json({ text: data.content?.[0]?.text || text });
  } catch (e) {
    return res.status(500).json({ error: e.message });
  }
}
